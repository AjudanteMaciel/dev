# Gpu usage
function info_gpu_usage {
    $usage = nvidia-smi --query-gpu=utilization.gpu --format=csv,noheader,nounits
    return @{
        title = "GPU Usage"
        content = "$usage%"
    }
}

# Custom weather
function info_custom_weather {
    $weatherRaw = (Invoke-RestMethod "https://wttr.in/Inhumas+GO?format=1").Trim()

    # Separar a primeira "palavra" (o emoji) do resto
    $parts = $weatherRaw -split '\s+', 2

    if ($parts.Count -eq 2) {
        $emoji = $parts[0]
        $text = $parts[1]
        $weather = "$text $emoji"
    } else {
        $weather = $weatherRaw
    }

    return @{
        title = "Weather"
        content = $weather
    }
}

# Custom dashes
function info_custom_dashes {
    $width = [console]::WindowWidth
    $half = [math]::Floor($width / 2)
    $dashes = '-' * $half
    return @{
        title = ""
        content = $dashes
    }
}

# Custom blank_line
function info_blankline_n {
    param([int]$count = 1)

    $lines = @()
    for ($i = 0; $i -lt $count; $i++) {
        $lines += @{ title = ""; content = "" }
    }
    return $lines
}

function info_blank_all {
    return info_blankline_n 21
}


